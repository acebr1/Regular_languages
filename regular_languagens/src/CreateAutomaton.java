    private void jBNovoEstadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBNovoEstadoActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();  
        boolean isFinal = jCFinal.isSelected();
        boolean isInitial = jCInicial.isSelected();
        String name = jTNovoEstado.getText();
        String fin="",ini="";
        if(isFinal){
            fin = "*";
        }
        if(isInitial){
            ini = "->";
        }
        model.addRow(new String[]{fin,ini,name});
        model.fireTableStructureChanged();
    }//GEN-LAST:event_jBNovoEstadoActionPerformed

